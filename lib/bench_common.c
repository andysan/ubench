#define _GNU_SOURCE

#include "bench_common.h"

#include <sched.h>

#include "expect.h"

int
bench_pin_cpu()
{
    if (bench_settings.cpu != -1) {
	cpu_set_t cpu_set;

	CPU_ZERO(&cpu_set);
	CPU_SET(bench_settings.cpu, &cpu_set);
	return sched_setaffinity(0, sizeof(cpu_set_t), &cpu_set);
    }

    return 0;
}


